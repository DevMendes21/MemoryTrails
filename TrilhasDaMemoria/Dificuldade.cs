namespace TrilhasDaMemoria
{
    /// <summary>
    /// Enum que define os níveis de dificuldade do jogo
    /// </summary>
    public enum Dificuldade
    {
        /// <summary>
        /// Nível Fácil: 4x4 = 16 cartas (8 pares)
        /// </summary>
        Facil,
        
        /// <summary>
        /// Nível Médio: 6x4 = 24 cartas (12 pares)
        /// </summary>
        Medio,
        
        /// <summary>
        /// Nível Difícil: 6x6 = 36 cartas (18 pares)
        /// </summary>
        Dificil
    }
}
